namespace Soyo.SoyoRuntimeConsole.ParameterHandlers
{
    /// <summary>
    /// 括号类型，用于 CompositeParameterHandler 指定参数包裹的括号样式。
    /// </summary>
    public enum BracketType
    {
        /// <summary>
        /// 圆括号 ()
        /// </summary>
        Parentheses,

        /// <summary>
        /// 花括号 {}
        /// </summary>
        Braces,

        /// <summary>
        /// 方括号 []
        /// </summary>
        Brackets
    }
}
